package mainPrograms;

public class Swap2NumbersUsing3rdvariable3 
{

	public static void main(String[] args) 
	{
		 
		 int first  = 10;     //20
		 int second = 20;     //10
		 int temp;            //10
		 
		 System.out.println("Before swap");
		 System.out.println("first number1 : "+first);
		 System.out.println("second number2 : "+second);
		 
		 temp=first;          //10
		 first=second;        //20
		 second=temp;         //10
		 
		 System.out.println("Afore swap");
		 System.out.println("first number1 : "+first);
		 System.out.println("second number2 : "+second);

	}

}
