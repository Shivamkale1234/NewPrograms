package MySimpleMainPrograms;

public class FactorsofNumber {
	
	public static void main(String[] args) {
		
	int num =20;
	
	System.out.print("display the factors of "+ num + " are:- ");
	
	for(int i=1; i<=num; i++)
	{
		if(num % i ==0)
		{
			System.out.print(i + " ");
		}
	}
}
}
//Display Factors of 20 are : 1, 2, 4, 5, 10, 20............

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	