package MySimpleMainPrograms;

public class ForloopEx5 {
	
	public static void main(String[] args) 
	{
		
		int a=0; 
		
		for(int i=1; i<=8; i++)
		{
		    a=a+i;
		}
		System.out.println(a);   
	} 
}
     
