package loops;

public class forloopEx2_printnumfrom10To1 {
	
	public static void main(String[] args) {
		
		//start num 10
		//end num 1
		//incr or decr by 1
		int count=0;
		int i=10;
		
		for(i=10;i>=1;i--)
		{
			count++;
			System.out.println(i);
			
			
		}System.out.println("------------------------");
		System.out.println(count);
	}
	

}
